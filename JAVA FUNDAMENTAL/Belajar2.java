public class Belajar2 {
    public static void main(String[] args) {
		int a = 1;
		int b = 2;
		
		// Increament Versi 1 //
		System.out.println(a++);
		System.out.println(a++);
		System.out.println(a);
		
		// Increament Versi 2 //
		System.out.println(++b);
		System.out.println(++b);
		System.out.println(b);
		
		// Increament Versi 3 //
		System.out.println(b++);
		System.out.println(b++);
		System.out.println(b);
		
    }
}